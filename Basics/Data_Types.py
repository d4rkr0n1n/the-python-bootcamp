# Data Types
x = 5
print(type(x))
y = "John"
print(type(y))
z = 6.9
print(type(z))
a = 1j
print(type(a))
b = ["apple", "banana", "cherry"]
print(type(b))
c = ("apple", "banana", "cherry")
print(type(c))
d = range(6)
print(type(d))
e = {"name" : "John", "age" : 36}
print(type(e))
f = {"apple", "banana", "cherry"}
print(type(f))
g = frozenset({"apple", "banana", "cherry"})
print(type(g))
h = True
print(type(h))
i = b"Hello"
print(type(i))
j = bytearray(5)
print(type(j))
k = memoryview(bytes(5))
print(type(k))
